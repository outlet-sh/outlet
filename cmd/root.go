package cmd

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "outlet",
	Short: "Outlet.sh - Self-hosted email marketing platform",
	Long: `Outlet.sh is a self-hosted email marketing and transactional email platform.

Run 'outlet serve' to start the server, or use other subcommands for management tasks.`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

